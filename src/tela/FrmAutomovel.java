    private void carregarTabela(List<Automovel> autos) {
        AutomovelTableModel tabela = new AutomovelTableModel(autos);
        tblAutomovel.setModel(tabela);
        tblAutomovel.setRowHeight(30);
        tblAutomovel.setAutoResizeMode(JTable.AUTO_RESIZE_LAST_COLUMN);
        tblAutomovel.getColumnModel().getColumn(0).setPreferredWidth(100);
        tblAutomovel.getColumnModel().getColumn(1).setPreferredWidth(200);
        tblAutomovel.getColumnModel().getColumn(2).setPreferredWidth(200);
        tblAutomovel.getColumnModel().getColumn(3).setPreferredWidth(200);
        tblAutomovel.getColumnModel().getColumn(4).setPreferredWidth(200);
        tblAutomovel.getColumnModel().getColumn(5).setPreferredWidth(200);
    }

    private void limparCampos() {
        txtAnoFabricacao.setText("");
        txtAnoModelo.setText("");
        txtConsultar.setText("");
        txtKilometragem.setText("");
        txtObservacoes.setText("");
        txtPreco.setText("");
    }

    private void ctrlBtn() {
        btnInserir.setEnabled(!btnInserir.isEnabled());
        btnAlterar.setEnabled(!btnAlterar.isEnabled());
        btnExcluir.setEnabled(!btnExcluir.isEnabled());
        btnCancelar.setEnabled(!btnCancelar.isEnabled());
    }
    
    private void carregarCombo() {
        
        DefaultComboBoxModel<Modelo> modeloComboModel = (DefaultComboBoxModel) cbModelo.getModel();

        for (Modelo obj : this.modelos) {            
            modeloComboModel.addElement(obj);
        }

        
    }
