    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
        // TODO add your handling code here:
        // Seto o nome da marca no Objeto marca
        this.auto.setAnoFabricacao(Integer.parseInt(txtAnoFabricacao.getText()));
        this.auto.setAnoModelo(Integer.parseInt(txtAnoModelo.getText()));
        this.auto.setKilometragem(Integer.parseInt(txtKilometragem.getText()));
        this.auto.setModelo((Modelo) cbModelo.getSelectedItem());
        this.auto.setObservacoes(txtObservacoes.getText());
        this.auto.setPreco(Double.parseDouble(txtPreco.getText()));
        // Chamo o metodo salvar do marcaDao passando o objeto marca como parametro
        this.autoDao.salvar(this.auto);
        // Recarrego a lista de marcas
        this.autos = autoDao.carregarTodos();
        // Recarrego a tabela de marcas da tela
        carregarTabela(autos);
        // Limpo os campos
        limparCampos();
        // Jogo o foco no campo marca da tela
        txtAnoFabricacao.requestFocus();
        // Apresento a mensagem na tela
        JOptionPane.showMessageDialog(this, "Salvo com sucesso!",
                "Informação", JOptionPane.INFORMATION_MESSAGE);

    }//GEN-LAST:event_btnInserirActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:

        this.auto.setAnoFabricacao(Integer.parseInt(txtAnoFabricacao.getText()));
        this.auto.setAnoModelo(Integer.parseInt(txtAnoModelo.getText()));
        this.auto.setKilometragem(Integer.parseInt(txtKilometragem.getText()));
        this.auto.setModelo((Modelo) cbModelo.getSelectedItem());
        this.auto.setObservacoes(txtObservacoes.getText());
        this.auto.setPreco(Double.parseDouble(txtPreco.getText()));
        this.autoDao.alterar(this.auto);
        // Recarrego a lista de marcas
        this.autos = autoDao.carregarTodos();
        // Recarrego a tabela de marcas da tela
        carregarTabela(this.autos);
        // Limpo os campos
        limparCampos();
        // Jogo o foco no campo marca da tela
        txtAnoFabricacao.requestFocus();
        JOptionPane.showMessageDialog(this, "Alterado com sucesso!",
                "Informação", JOptionPane.INFORMATION_MESSAGE);

        ctrlBtn();
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:

        int selectedOption = JOptionPane.showConfirmDialog(null, "Tem certeza?", "Sistema informa:", JOptionPane.YES_NO_OPTION);
        if (selectedOption == JOptionPane.YES_OPTION) {

            this.autoDao.excluir(this.auto);
            // Recarrego a lista de marcas
            this.autos = autoDao.carregarTodos();
            // Recarrego a tabela de marcas da tela
            carregarTabela(this.autos);
            // Limpo os campos
            limparCampos();
            // Jogo o foco no campo marca da tela
            txtAnoFabricacao.requestFocus();
            ctrlBtn();
            JOptionPane.showMessageDialog(this, "Excluido com sucesso",
                    "Informação", JOptionPane.INFORMATION_MESSAGE);
        }


    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        // TODO add your handling code here:
        limparCampos();
        txtAnoFabricacao.requestFocus();
        this.auto = new Automovel();
        ctrlBtn();
    }//GEN-LAST:event_btnCancelarActionPerformed

    private void tblAutomovelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAutomovelMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() > 1) {
            
            this.auto = this.autos.get(tblAutomovel.convertRowIndexToModel(tblAutomovel.getSelectedRow()));
            txtAnoFabricacao.setText(this.auto.getAnoFabricacao().toString());
            txtAnoModelo.setText(this.auto.getAnoModelo().toString());
            txtKilometragem.setText(this.auto.getKilometragem().toString());
            txtObservacoes.setText(this.auto.getObservacoes());
            txtPreco.setText(this.auto.getPreco().toString());
            cbModelo.setSelectedItem(this.auto.getModelo());
            ctrlBtn();
        }
    }//GEN-LAST:event_tblAutomovelMouseClicked


