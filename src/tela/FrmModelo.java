    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
        // TODO add your handling code here:
        // Seto o nome da marca no Objeto marca
        this.modelo.setDescricao(txtDescricao.getText());
        this.modelo.setPotencia(Integer.parseInt(txtPotencia.getText()));
        this.modelo.setMarca((Marca) cbMarca.getSelectedItem());
        // Chamo o metodo salvar do marcaDao passando o objeto marca como parametro
        this.modeloDao.salvar(this.modelo);
        // Recarrego a lista de marcas
        this.modelos = modeloDao.carregarTodos();
        // Recarrego a tabela de marcas da tela
        carregarTabela(modelos);
        // Limpo os campos
        limparCampos();
        // Jogo o foco no campo marca da tela
        txtDescricao.requestFocus();
        // Apresento a mensagem na tela
        JOptionPane.showMessageDialog(this, "Salvo com sucesso!",
            "Informação", JOptionPane.INFORMATION_MESSAGE);

    }//GEN-LAST:event_btnInserirActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:

        this.modelo.setDescricao(txtDescricao.getText());
        this.modelo.setMarca((Marca) cbMarca.getSelectedItem());
        this.modeloDao.alterar(this.modelo);
        // Recarrego a lista de modelos
        this.modelos = modeloDao.carregarTodos();
        // Recarrego a tabela de modelos da tela
        carregarTabela(modelos);
        // Limpo os campos
        limparCampos();
        // Jogo o foco no campo descricao da tela
        txtDescricao.requestFocus();
        JOptionPane.showMessageDialog(this, "Alterado com sucesso!",
            "Informação", JOptionPane.INFORMATION_MESSAGE);

        ctrlBtn();
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        if (this.modelo.getAutomoveis().size() > 0) {
            JOptionPane.showMessageDialog(this, "Essa marca possui automoveis(s) relacionado(s)",
                "Informação", JOptionPane.ERROR_MESSAGE);
        } else {
            int selectedOption = JOptionPane.showConfirmDialog(null, "Tem certeza?", "Sistema informa:", JOptionPane.YES_NO_OPTION);
            if (selectedOption == JOptionPane.YES_OPTION) {

                this.modeloDao.excluir(this.modelo);
                // Recarrego a lista de modelos
                this.modelos = modeloDao.carregarTodos();
                // Recarrego a tabela de marcas da tela
                carregarTabela(this.modelos);
                // Limpo os campos
                limparCampos();
                // Jogo o foco no campo marca da tela
                txtDescricao.requestFocus();
                ctrlBtn();
                JOptionPane.showMessageDialog(this, "Excluido com sucesso",
                    "Informação", JOptionPane.INFORMATION_MESSAGE);
            }

        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        // TODO add your handling code here:
        limparCampos();
        txtDescricao.requestFocus();
        this.modelo = new Modelo();
        ctrlBtn();
    }//GEN-LAST:event_btnCancelarActionPerformed

    private void tblModeloMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblModeloMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() > 1) {
            // Pego a linha selecionada da tabela e jogo pro objeto
            this.modelo = this.modelos.get(tblModelo.convertRowIndexToModel(tblModelo.getSelectedRow()));
            // Jogo o nome da marca na caixa de texto
            txtDescricao.setText(this.modelo.getDescricao());
            txtPotencia.setText(this.modelo.getPotencia().toString());
            cbMarca.setSelectedItem(this.modelo.getMarca());
            ctrlBtn();
        }
    }//GEN-LAST:event_tblModeloMouseClicked

    private void txtConsultarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtConsultarKeyReleased
        // TODO add your handling code here:
        
        this.modelos = modeloDao.carregarPorDescricao(txtConsultar.getText());
        carregarTabela(this.modelos);
    }//GEN-LAST:event_txtConsultarKeyReleased


