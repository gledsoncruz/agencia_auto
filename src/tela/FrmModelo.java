    private void limparCampos() {
        txtDescricao.setText("");
        txtPotencia.setText("");
        txtConsultar.setText("");
        cbMarca.setSelectedIndex(0);
    }

    private void ctrlBtn() {
        btnInserir.setEnabled(!btnInserir.isEnabled());
        btnAlterar.setEnabled(!btnAlterar.isEnabled());
        btnExcluir.setEnabled(!btnExcluir.isEnabled());
        btnCancelar.setEnabled(!btnCancelar.isEnabled());
    }

    private void carregarTabela(List<Modelo> modelos) {
        ModeloTableModel tabela = new ModeloTableModel(modelos);
        tblModelo.setModel(tabela);
        tblModelo.setRowHeight(30);
        tblModelo.setAutoResizeMode(JTable.AUTO_RESIZE_LAST_COLUMN);
        tblModelo.getColumnModel().getColumn(0).setPreferredWidth(100);
        tblModelo.getColumnModel().getColumn(1).setPreferredWidth(400);
        tblModelo.getColumnModel().getColumn(2).setPreferredWidth(400);
        tblModelo.getColumnModel().getColumn(3).setPreferredWidth(400);
    }
    
    private void carregarCombo() {
        
        DefaultComboBoxModel<Marca> marcaComboModel = (DefaultComboBoxModel) cbMarca.getModel();

        for (Marca obj : this.marcas) {            
            marcaComboModel.addElement(obj);
        }

        
    }
